function series(index_code: string, unit: BalticIndexRow["unit"], start: number, slope: number): BalticIndexRow[] {
  return Array.from({ length: 20 }, (_, index) => ({
    date: format(addDays(new Date("2026-05-01T00:00:00Z"), index), "yyyy-MM-dd"),
    index_code,
    unit,
    value: Math.round((start + slope * index + Math.sin(index / 2) * slope * 1.8) * 100) / 100,
  }));
}
